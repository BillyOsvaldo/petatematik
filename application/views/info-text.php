<h3>Orang Dalam Pemantauan (ODP)</h3>
<p>Adalah Seseorang yang baru saja datang dari daerah-daerah terjangkit baik di dalam maupun luar negeri atau kontak dengan orang yang terpapar covid tetapi menunjukan gejala suhu lebih dari 38 derajat celcius dan atau batuk tanpa sesak nafas.</p>

<br />

<h3>Pasien Dalam Pemantauan (PDP)</h3>
<p>Merupakan pasien yang menunjukan tanda dan gejala Covid-19, panas, batuk, pilek, sakit tenggorokan, sesak nafas dan pernah berkunjung ke daerah terjangkit dan atau kontak dengan orang terpapar Covid-19.</p>

